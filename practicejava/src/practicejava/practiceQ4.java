package practicejava;
//Print the following star pattern using nested for loops:

public class practiceQ4 {
	
	public static void main(String[]argc) {
		
		
		
		for (int i = 1; i <= 5; i++) {
		    for (int j = 1; j <= i; j++) {
		        System.out.print("*");
		    }
		    System.out.println();
		}
		
	}
	
	
}
